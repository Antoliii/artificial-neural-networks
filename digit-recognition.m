sPrime = zeros(N,2);

% update one bit at a time
% while loop until all bits are unchanged after 5 updates
while ~all(sPrime(:,2) > 5)
    for bit = 1:N
        if weightMatrix(bit,:)*s ~= 0
            sPrime(bit,1) = sign(weightMatrix(bit,:)*s);
        else
            sPrime(bit,1) = 1;
        end

        % check stability
        if sPrime(bit,1)/s(bit) == 1 % no change
            sPrime(bit,2) = sPrime(bit,2) + 1; % count no change streaks
        else
            sPrime(bit,2) = 0;
        end

        % reset
        s(bit) = sPrime(bit,1);
    end
end

