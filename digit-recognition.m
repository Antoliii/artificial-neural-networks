[N, p] = size(patterns);

% weight matrix using hebbs rule
weightMatrix = zeros(N,N);
% typewriter scheme:
for pattern = 1:p
    tempMatrix = zeros(N,N);

    for col = 1:N % column
        for row = 1:N % row


            if col ~= row
                hebs = 1/N * patterns(col,pattern) * patterns(row,pattern);
                tempMatrix(row, col) = hebs;
            else
                tempMatrix(row, col) = 0;
            end


        end
    end

    weightMatrix = weightMatrix + tempMatrix;
end

% set diagonal to 0
weightMatrix(1:1+size(weightMatrix,1):end) = 0;

% test convergance
